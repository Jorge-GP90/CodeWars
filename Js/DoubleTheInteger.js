// Code as fast as you can! You need to double the integer and return it.

function doubleInteger(i) {
  return i * 2;
}

// Second way (arrow functions)

const doubleInteger = (i) => 2 * i;

// third way

function doubleInteger(i) {
  return i + i;
}

// Fourth way
function doubleInteger(i) {
  return i << 1;
}
